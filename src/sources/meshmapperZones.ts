// How many regions we are willing to fetch for one sync. A zoomed-out view can overlap many region
// centers; beyond this we keep the nearest ones and flag `capped` so the UI can suggest zooming in.
const MAX_ZONES = 25;
// Always also pull the few nearest regions (within NEAREST_MAX_DEG of the view centre), even when the
// margin test matches nothing. MeshMapper's published zone "centre" is a data centroid that can sit
// >1° from where its repeaters actually are (e.g. WLG's centre is up by Palmerston North, well north
// of Wellington city), so a margin-only test misses the home region on a tight view. Over-fetching a
// handful of small region files is cheap — the orchestrator clips every node to the exact bbox anyway.
const NEAREST_COUNT = 8;
const NEAREST_MAX_DEG = 3;

function distSq(z: MeshMapperZone, cx: number, cy: number): number {
  return (z.lat - cy) ** 2 + (z.lon - cx) ** 2;
}

// Resolve the current view to the region subdomains worth fetching: the union of zones whose centre
// falls inside `bbox` grown by a margin (covers zoomed-out views) and the nearest few zones to the
// view centre (covers tight views where the home region's centroid is offset outside the margin).
// Antimeridian-spanning views aren't handled (acceptable — the populated regions don't straddle it).
export function zonesForBounds(bbox: Bbox): { codes: string[]; capped: boolean } {
  const margin = Math.max((bbox.north - bbox.south) / 2, (bbox.east - bbox.west) / 2, 0.75);
  const cx = (bbox.east + bbox.west) / 2;
  const cy = (bbox.north + bbox.south) / 2;

  const byCode = new Map<string, MeshMapperZone>();
  for (const z of MESHMAPPER_ZONES) {
    if (
      z.lat >= bbox.south - margin &&
      z.lat <= bbox.north + margin &&
      z.lon >= bbox.west - margin &&
      z.lon <= bbox.east + margin
    ) {
      byCode.set(z.code, z);
    }
  }

  const nearest = MESHMAPPER_ZONES.map((z) => ({ z, d: distSq(z, cx, cy) }))
    .filter((o) => o.d <= NEAREST_MAX_DEG * NEAREST_MAX_DEG)
    .sort((a, b) => a.d - b.d)
    .slice(0, NEAREST_COUNT);
  for (const o of nearest) {
    byCode.set(o.z.code, o.z);
  }

  let selected = [...byCode.values()];
  const capped = selected.length > MAX_ZONES;
  if (capped) {
    selected = selected.sort((a, b) => distSq(a, cx, cy) - distSq(b, cx, cy)).slice(0, MAX_ZONES);
  }

  return { codes: selected.map((z) => z.code), capped };
}
